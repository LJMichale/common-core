package common.core.common.assertion.exception;

public class InfoAssertionException extends AssertionException {
	private static final long serialVersionUID = 7805870621322339130L;

	public InfoAssertionException() {
		super();
	}

	public InfoAssertionException(String message, Throwable cause) {
		super(message, cause);
	}

	public InfoAssertionException(String message) {
		super(message);
	}

	public InfoAssertionException(Throwable cause) {
		super(cause);
	}

}
