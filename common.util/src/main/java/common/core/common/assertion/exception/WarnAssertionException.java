package common.core.common.assertion.exception;

public class WarnAssertionException extends AssertionException {
	private static final long serialVersionUID = 7805870621322339130L;

	public WarnAssertionException() {
		super();
	}

	public WarnAssertionException(String message, Throwable cause) {
		super(message, cause);
	}

	public WarnAssertionException(String message) {
		super(message);
	}

	public WarnAssertionException(Throwable cause) {
		super(cause);
	}

}
