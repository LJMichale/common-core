package common.core.common.assertion.exception;

public class ErrorAssertionException extends AssertionException {
	private static final long serialVersionUID = 7805870621322339130L;

	public ErrorAssertionException() {
		super();
	}

	public ErrorAssertionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ErrorAssertionException(String message) {
		super(message);
	}

	public ErrorAssertionException(Throwable cause) {
		super(cause);
	}

}
